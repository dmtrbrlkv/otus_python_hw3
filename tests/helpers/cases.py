import functools
import sys


def cases(cases):
    def decorator(f):
        @functools.wraps(f)
        def wrapper(*args):
            for c in cases:
                new_args = args + (c if isinstance(c, tuple) else (c,))
                try:
                    f(*new_args)
                except Exception as e:
                    print(f"Case: {c}", file=sys.stderr)
                    raise
        return wrapper
    return decorator
